import os


def set_path():
    pathlist = os.environ['PATH'].split(os.pathsep)

    for x in ('/sbin', '/usr/sbin', '/usr/local/sbin'):
        if x not in pathlist:
            pathlist.append(x)

    os.environ['PATH'] = os.pathsep.join(pathlist)
